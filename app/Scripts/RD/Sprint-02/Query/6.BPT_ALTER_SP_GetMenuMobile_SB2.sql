USE BelcorpPeru
GO

ALTER PROCEDURE [dbo].[GetMenuMobile_SB2]
AS
BEGIN
SET NOCOUNT ON;
	SELECT 
		MenuMobileID
		,Descripcion
		,MenuPadreID
		,OrdenItem
		,UrlItem
		,UrlImagen
		,PaginaNueva
		,Posicion
		,[Version]
		,EsSB2
		,Codigo
	FROM dbo.MenuMobile
	WHERE EsSB2=1
	order by Posicion, MenuPadreID, ordenItem
END
GO
/*end*/

