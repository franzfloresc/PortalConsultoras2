 ([Codigo]
      ,[Descripcion]
      ,[Orden]
      ,[CodigoMenuPadre]
      ,[Posicion]
      ,[Visible]
      ,[VersionMenu]
      ,[Descripcion2]
      ,[Descripcion3])
