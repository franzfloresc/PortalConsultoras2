USE BelcorpBolivia
GO
UPDATE CDRWebDescripcion SET Descripcion = 'REENVÍO DEL MISMO PRODUCTO' WHERE descripcion like '%REENVÍO%' AND CodigoSSIC = 'C'
UPDATE CDRWebDescripcion SET Descripcion = 'DEVOLUCIÓN' WHERE descripcion like '%DEVOLUCIÓN%' AND TIPO = 'Solucion' and descripcion not like '%DINERO%'
UPDATE CDRWebDescripcion SET Descripcion = 'Devolución del mismo producto' WHERE descripcion like '%DEVOLUCIÓN%' AND CodigoSSIC = 'D' and descripcion not like '%DINERO%'  AND tipo = 'Finalizado'
UPDATE CDRWebDescripcion SET Descripcion = 'Reenvío del mismo producto' WHERE descripcion like '%Reenvío%' AND CodigoSSIC = 'C'  AND tipo = 'Finalizado' 
GO


