USE BelcorpPeru
GO

CREATE PROCEDURE [dbo].[ContenidoAppDetaActList]
AS
BEGIN
	
	SELECT
		P.IdContenidoAct,
		P.Codigo,
		P.Descripcion,
		P.Parent,
		P.Orden,
		P.Activo		
	FROM dbo.ContenidoAppDetaAct AS P with(nolock)
	WHERE
		P.Activo = 1
		order by P.Orden;
END

GO

