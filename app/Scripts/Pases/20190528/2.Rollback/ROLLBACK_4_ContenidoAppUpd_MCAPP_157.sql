USE BelcorpPeru
GO

ALTER PROCEDURE [dbo].[ContenidoAppUpd]
@IdContenido INT,
@UrlMiniatura VARCHAR(100),
@DesdeCampania INT
AS
BEGIN
		UPDATE ContenidoApp 
		SET
		UrlMiniatura = @UrlMiniatura,
		DesdeCampania = @DesdeCampania
		WHERE 
		IdContenido = @IdContenido;
END

GO

