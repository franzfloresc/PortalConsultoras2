USE BelcorpPeru
GO

IF EXISTS (SELECT 1 FROM SYSOBJECTS WHERE TYPE = 'U' and id = OBJECT_ID('dbo.ConsultoraCaminoBrillante'))
	DROP TABLE dbo.ConsultoraCaminoBrillante
GO
CREATE TABLE dbo.ConsultoraCaminoBrillante(
	ConsultoraID BIGINT,
	PeriodoCB INT,
	Campania INT,
	NivelCB INT,
	CambioNivelCB int,
	FlagOnboardingAnim BIT,
	FlagOnboardingAnimRepeat BIT,
	FlagGananciaAnim BIT,
	FlagCambioNivelAnim BIT,
	FechaRegistro DATETIME,
	FechaEdicion DATETIME
	PRIMARY KEY (ConsultoraID)
)
GO

