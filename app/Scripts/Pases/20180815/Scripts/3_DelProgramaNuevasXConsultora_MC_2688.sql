USE BelcorpBolivia
GO
IF OBJECT_ID('dbo.DelProgramaNuevasXConsultora') IS NOT NULL
BEGIN
	drop procedure dbo.DelProgramaNuevasXConsultora
END
GO
CREATE PROCEDURE dbo.DelProgramaNuevasXConsultora
	@CodigoConsultora varchar(15)
AS
BEGIN
	set nocount on;

	delete from dbo.IncentivosConsultoraProgramaNuevas
	where CodigoConsultora = @CodigoConsultora;
END
GO
