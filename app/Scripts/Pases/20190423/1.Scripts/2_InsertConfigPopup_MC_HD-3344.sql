USE [BelcorpBolivia];
GO

IF NOT EXISTS (SELECT 1 
               FROM   tablalogica 
               WHERE  tablalogicaid = 171) 
  BEGIN 
      INSERT INTO dbo.tablalogica 
                  (tablalogicaid, 
                   descripcion) 
      VALUES      ( 171, 
                    'Popup informativo' ); 
  END 

go 

IF NOT EXISTS (SELECT 1 
               FROM   tablalogicadatos 
               WHERE  tablalogicaid = 171) 
  BEGIN 
