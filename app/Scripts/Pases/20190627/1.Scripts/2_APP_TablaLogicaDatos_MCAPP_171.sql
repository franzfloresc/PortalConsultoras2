USE BelcorpPeru
GO

DECLARE @ID INT
SET @ID= (SELECT MAX(TablaLogicaDatosID) FROM TablaLogicaDatos)
SET @ID=@ID+1

IF (NOT EXISTS(SELECT * FROM [dbo].[TablaLogicaDatos] WHERE Codigo='DESACTIVA_COMISION_VISA'))
BEGIN

	INSERT INTO [dbo].[TablaLogicaDatos]
			([TablaLogicaDatosID],[TablaLogicaID],[Codigo],[Descripcion],[Valor])
     VALUES
           (@ID,122,'DESACTIVA_COMISION_VISA','Coloca comision en 0 para las consultoras digitales antes de su fecha de vencimiento',0)
END

