USE BelcorpPeru
GO
go

begin
	if(exists(select 1 from dbo.configuracionpais where codigo = 'MG'))
	begin
		update
			dbo.configuracionpais
		set
			 orden			= orden			 - 2
			,ordenbpt		= ordenbpt		 - 2
			,mobileorden	= mobileorden	 - 2
			,mobileordenbpt = mobileordenbpt - 2
		where
			not orden in(0,1);

	end;
end;
