USE BelcorpPeru
GO

If Exists(select 1 from SYSOBJECTS where type = 'P' and id = OBJECT_ID('dbo.GetBeneficioCaminoBrillante'))
Begin
	DROP PROCEDURE [dbo].[GetBeneficioCaminoBrillante]
end
GO
CREATE PROCEDURE [dbo].[GetBeneficioCaminoBrillante]
AS
BEGIN
	SELECT 
		 CodigoNivel AS CodigoNivel
		,CodigoBeneficio AS CodigoBeneficio
		,NombreBeneficio AS NombreBeneficio
		,Descripcion AS Descripcion
		,UrlIcono AS Icono
	FROM  dbo.BeneficioCaminoBrillante WITH (NOLOCK)
	Order By Orden asc
END
GO

