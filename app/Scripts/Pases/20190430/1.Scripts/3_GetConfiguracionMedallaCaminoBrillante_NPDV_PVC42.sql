USE BelcorpPeru
GO

If Exists(select 1 from SYSOBJECTS where type = 'P' and id = OBJECT_ID('dbo.GetConfiguracionMedallaCaminoBrillante'))
Begin
	DROP PROCEDURE [dbo].[GetConfiguracionMedallaCaminoBrillante]
end
GO
CREATE procedure [dbo].[GetConfiguracionMedallaCaminoBrillante]
as
begin

SELECT   
   Logro AS Logro, 
   Indicador AS Indicador, 
   Codigo AS Codigo, 
   Valor AS Valor, 
   ComoLograrlo_Estado AS ComoLograrlo_Estado, 
   ComoLograrlo_Titulo AS ComoLograrlo_Titulo, 
   ComoLograrlo_Descripcion AS ComoLograrlo_Descripcion, 
   Estado Estado
 FROM  ConfiguracionMedallaCaminoBrillante  WITH (NOLOCK)
 ORDER BY Logro, Indicador, Orden

 END
GO

