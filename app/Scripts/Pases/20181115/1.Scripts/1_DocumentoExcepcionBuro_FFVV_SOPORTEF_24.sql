USE BelcorpPeru
GO

IF EXISTS(SELECT 1 FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME  = 'DocumentoExcepcionBuro')
BEGIN
   DROP TABLE DocumentoExcepcionBuro
END
GO

create table DocumentoExcepcionBuro(
	DocumentoExcepionBuroID int,
	NumeroDocumento varchar(30),
	TipoDocumento varchar(2),
	Activo bit,
	UsuarioCreacion varchar(20),
	FechaCreacion datetime,
	UsuarioEliminacion  varchar(20),
	FechaEliminacion datetime
)
GO

