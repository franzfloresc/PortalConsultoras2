USE BelcorpPeru
GO

IF EXISTS (SELECT * FROM sys.objects WHERE object_id = OBJECT_ID(N'[dbo].[UpdateImporteTotalPedidoWeb]') AND type in (N'P', N'PC')) 
	DROP PROCEDURE [dbo].UpdateImporteTotalPedidoWeb
GO

create procedure dbo.UpdateImporteTotalPedidoWeb
@CampaniaId int,
@ConsultoraId bigint,
@Importe decimal(18,2)
as
begin

update PedidoWeb
set ImporteTotal = @Importe
where CampaniaId = @CampaniaId and ConsultoraId = @ConsultoraId

end

go

