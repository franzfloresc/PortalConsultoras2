USE BelcorpPeru
GO

CREATE procedure [dbo].[GetConsultoraPorCodigoYRowCount]
@codigo varchar(25),
@rowCount int
as

select top (@rowCount) ConsultoraID, 

	   Codigo, 

	   0 RegionID, 

	   0 ZonaID, 

	   0 TerritorioID

from ods.Consultora where Codigo like @Codigo + '%'
go

