

USE BelcorpBolivia
GO

IF EXISTS
(
    SELECT 1
    FROM ShowRoom.Personalizacion
    WHERE TipoAplicacion = 'Desktop'
          AND Atributo = 'ImagenFondoOfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'
)
BEGIN

	DELETE FROM ShowRoom.Personalizacion 
	WHERE TipoAplicacion = 'Desktop'
          AND Atributo = 'ImagenFondoOfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'

	DELETE FROM ShowRoom.Personalizacion 
	WHERE TipoAplicacion = 'Desktop'
          AND Atributo = 'Titulo1OfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'
		  
	DELETE FROM ShowRoom.Personalizacion 
	WHERE TipoAplicacion = 'Mobile'
          AND Atributo = 'ImagenFondoOfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'
    
    DELETE FROM ShowRoom.Personalizacion 
	WHERE TipoAplicacion = 'Mobile'
          AND Atributo = 'Titulo1OfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'
	
	DELETE FROM ShowRoom.Personalizacion 
	WHERE TipoAplicacion = 'Mobile'
          AND Atributo = 'ColorFondo1OfertaFinalRegalo'
          AND TipoPersonalizacion = 'EVENTO'
   
