USE BelcorpBolivia
GO

CREATE PROCEDURE UpdMatrizComercialDescripcionComercial
(
	@IdMatrizComercialImagen varchar(50),
	@UsuarioModificacion varchar(50),
	@DescripcionComercial varchar(800)
)
AS
UPDATE MatrizComercialImagen
	SET
		UsuarioModificacion = @UsuarioModificacion,
		FechaModificacion = getdate(),
		DescripcionComercial = @DescripcionComercial
	WHERE IdMatrizComercialImagen = @IdMatrizComercialImagen ;
	GO

