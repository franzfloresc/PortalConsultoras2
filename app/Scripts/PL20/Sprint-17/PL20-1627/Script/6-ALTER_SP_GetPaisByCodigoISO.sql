use BelcorpPeru 
go
alter procedure dbo.GetPaisByCodigoISO
@CodigoISO char(2)
as
/*
GetPaisByCodigoISO 'PE'
*/
begin

select PaisID, Nombre, Simbolo, OfertaFinal = 0, OFGanaMas = 0, HoraCierreZonaNormal, HoraCierreZonaDemAnti 
from dbo.Pais 
where CodigoISO = @CodigoISO

end

