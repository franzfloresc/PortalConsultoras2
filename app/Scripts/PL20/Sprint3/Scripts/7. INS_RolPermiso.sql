USE BelcorpBolivia
GO
if not exists(select 1 from RolPermiso where RolID = 1 and PermisoID = 1055)
begin
	insert into RolPermiso(RolID,PermisoID,Activo,Mostrar)
	values (1,1055,1,1);
end
else
begin
	update RolPermiso
	set
		Activo = 1,
		Mostrar = 1
	where RolID = 1 and PermisoID = 1055;
end
