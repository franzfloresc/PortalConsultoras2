use [BelcorpBolivia]
GO
CREATE PROC GetProductoCompartido
(
@ProductoCompID int
)
AS
BEGIN
	select 
		ProductoCompID,
		CampaniaID,
		CUV,
		Palanca,
		Detalle,
		Applicacion
	 from dbo.ProductoCompartido 
	 where ProductoCompID = @ProductoCompID
END
go



