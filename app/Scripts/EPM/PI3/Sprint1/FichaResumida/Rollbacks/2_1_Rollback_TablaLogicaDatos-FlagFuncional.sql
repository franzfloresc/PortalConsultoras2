USE BelcorpPeru
GO


print db_name()

declare @TablaLogicaId int = 158
declare @Codigo varchar(30) = 'MisClientes'


if exists (	select 1
				from TablaLogica tl
				where tl.TablaLogicaID = @TablaLogicaId)
begin

		if exists (	select 1
							from TablaLogicaDatos tl
							where tl.Codigo = @Codigo)
		begin

			print 'delete TablaLogicaDatos : 158 - Codigo: MisClientes'
			delete from TablaLogicaDatos where TablaLogicaID = @TablaLogicaId and Codigo = @Codigo

		end

end



