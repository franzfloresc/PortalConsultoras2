use BelcorpBolivia;
go

begin
	/* ShowRoom 
	 */
	if(exists(select * from dbo.ConfiguracionPais where Codigo = 'SR'))
	begin
		update
			dbo.configuracionofertashome 
		set
			desktopcantidadproductos = 3,mobilecantidadproductos = 3
		where 
			((ConfiguracionPaisID = (select ConfiguracionPaisID from dbo.ConfiguracionPais where Codigo = 'SR')) and 
			 (CampaniaID < 201817));
	end;
end;

