		DesktopSubTituloBanner,
		MobileSubTituloBanner,
		DesktopFondoBanner,
		MobileFondoBanner,
		DesktopLogoBanner,
		MobileLogoBanner,
		UrlMenu,
		OrdenBpt
	FROM ConfiguracionPais AS P with(nolock)
	WHERE 
		P.TienePerfil = @TienePerfil
END

