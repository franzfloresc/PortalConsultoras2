USE BelcorpPeru
GO

IF Exists (select 1 from SYSOBJECTS where type = 'P' and id = OBJECT_ID('dbo.GetBeneficioCaminoBrillante'))
	Drop Proc dbo.GetBeneficioCaminoBrillante
GO
CREATE PROC dbo.GetBeneficioCaminoBrillante
AS
BEGIN
	SELECT 
		 CodigoNivel
		,CodigoBeneficio
		,NombreBeneficio
		,Descripcion
		,UrlIcono
	FROM  dbo.BeneficioCaminoBrillante
END
GO

