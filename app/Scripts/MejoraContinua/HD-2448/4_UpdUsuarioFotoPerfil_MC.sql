USE BelcorpPeru
GO
alter PROCEDURE dbo.UpdUsuarioFotoPerfil
@CodigoUsuario VARCHAR(20),
@FotoPerfil VARCHAR(255)
AS
BEGIN
	UPDATE dbo.Usuario
	SET FotoPerfil = @FotoPerfil
	WHERE CodigoConsultora = @CodigoUsuario
END


