

USE BelcorpBolivia
GO

IF EXISTS(SELECT 1 FROM sys.sequences
		WHERE OBJECT_ID = OBJECT_ID('dbo.SecuenciaTokenPedido'))
BEGIN
	DROP SEQUENCE dbo.SecuenciaTokenPedido
END
GO

CREATE SEQUENCE [dbo].[SecuenciaTokenPedido] 
 AS [int]
 START WITH 1
 INCREMENT BY 1
 MINVALUE 1
 MAXVALUE 2147483647
 CACHE  32 
