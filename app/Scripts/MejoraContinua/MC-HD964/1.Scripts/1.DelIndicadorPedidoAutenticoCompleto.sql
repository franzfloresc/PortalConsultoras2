USE BelcorpPeru
GO

IF EXISTS(SELECT 1 FROM sys.procedures 
		WHERE object_id = OBJECT_ID(N'dbo.DelIndicadorPedidoAutenticoCompleto'))
BEGIN
    DROP PROCEDURE dbo.DelIndicadorPedidoAutenticoCompleto
END
GO


CREATE PROCEDURE DelIndicadorPedidoAutenticoCompleto
(
	@PedidoID INT,
	@CampaniaID INT
)
AS
BEGIN
	DELETE FROM IndicadorPedidoAutentico
	WHERE PedidoID = @PedidoID
	AND CampaniaID = @CampaniaID
	
END

GO

