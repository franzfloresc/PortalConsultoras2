USE BelcorpBolivia
GO
ALTER PROCEDURE dbo.GetNivelesProgramaNuevasByCampania
	@Campania varchar(6)
AS
BEGIN
	set nocount on;

	select
		CodigoPrograma,
		Campania,
		CodigoNivel,
		UnidadesNivel,
		UnidadesNivelElectivo
	from ods.NivelesProgramaNuevas
	where Campania = @Campania;
END
GO

