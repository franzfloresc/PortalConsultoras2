USE BelcorpBolivia
GO
alter procedure dbo.UpdCdrWebDetalleCantidadObservado
@CDRWebDetalleID int,
@Cantidad int
as
begin

update CDRWebDetalle
set
	Cantidad = @Cantidad
where 
	CDRWebDetalleID = @CDRWebDetalleID
	
end
go

