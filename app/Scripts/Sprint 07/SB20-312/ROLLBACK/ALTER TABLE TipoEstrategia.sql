USE BelcorpPeru
GO

IF EXISTS (
  SELECT 1 
  FROM   sys.columns 
  WHERE  object_id = OBJECT_ID(N'[dbo].[TipoEstrategia]') 
         AND name = 'FlagMostrarImg'
)
BEGIN
	ALTER TABLE [dbo].[TipoEstrategia] DROP COLUMN FlagMostrarImg
END

